# print：输出语句
# 作用：将信息在控制台中打印并展示。
# print：可以打印任何类型的内容

# 打印一个字符串
print("床前明月光")

# 打印多条数据，用 ,(逗号) 隔开，见到 ,(逗号)会有一个空格
print("疑是地上霜", "举头望明月", "低头认错")

# 打印一个数字
print(123)

# 混合打印
print("100+200 =", 300)

# 可以进行数学运算，将运算的结果打印
print(100 + 200)  # 300
print("100+200")  # 字符串中的运算符被当成普通字符使用

# print() 默认自动换行
# 如果使用print输出不想换行，在print的最后加一个end参数
# end: 设置当前输出语句以什么字符串结尾，end后面的数据类型为字符串类型。
# 可以认为end后面的值默认为 \n （换行 next）
print("hello", end="---")
print("world", end="\n")
print("!")
















