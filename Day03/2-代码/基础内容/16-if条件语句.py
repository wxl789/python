# if语句
'''
if 条件:
    语句
逻辑关系：当程序执行到if语句时，会判断条件是否成立：条件成立，则执行
if下面的语句；条件不成立，跳过if判断，继续向下执行。
条件：会得出一个为真或为假的结论
假：0    0.0     False  None    ""    ''
真：除了假就是真
'''

print("即将开始升温")
