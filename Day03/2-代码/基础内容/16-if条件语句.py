# if - else 语句
'''
if 条件:
    语句1
else:
    语句2
逻辑关系：当程序执行到if语句时，会判断条件是否成立：条件成立，则执行
if下面的语句1；条件不成立，则执行else下面的语句2
条件：会得出一个为真或为假的结论
假：0    0.0     False  None    ""    ''
真：除了假就是真
'''
sex = "女"
if sex == "男":
    print("进男厕所")
else:
    print("进女厕所")

print("呵呵")

# 判断输入的数字是否为偶数，为偶数则输出棒棒的，奇数输出菜菜的
num = input("请输入数字：")
if int(num) % 2 == 0:
    print("棒棒的")
else:
    print("菜菜的")

print(type(num))


