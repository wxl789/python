# 布尔值： True 与 False
b1 = True
b2 = False
print(b1)
print(type(b1))  # bool

# 空值： None python中特殊的值
# None 与 0  或 "" 一样吗？  不一样
# None： NoneType
# 0 ： int
# "" : str

# 但都表示假

n1 = None
print(n1)  # None
print(type(n1))   # NoneType  空值类型

if 0:
    print("0000")
if None:
    print("None")
